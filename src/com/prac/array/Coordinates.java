package com.prac.array;

public class Coordinates {
    int width;
    int height;

    public Coordinates(int width, int height){
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setY(int height) {
        this.height = height;
    }
}
